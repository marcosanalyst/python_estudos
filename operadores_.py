# Operadores

#        == Equal (igual)
#        != Not Equal (diferente ou não igual)
#        >  Greater than (maior que)
#        <  Less than (menor que)
#        >= Greater than or equal to (maior que ou igual a)
#        <= Less than or equal to (menor que ou igual a )


operadores = 10 == 10
print(operadores)
operadores = 10 != 10
print(operadores)
operadores = 10 > 10
print(operadores)
operadores = 10 < 10
print(operadores)
operadores = 10 >= 10
print(operadores)
operadores = 10 <= 10
print(operadores)

