# Conceitos básicos de Operação Matemáticas

calculo = 9 + 6
print(calculo)
calculo = 9 - 6
print(calculo)
calculo = 9 * 6
print(calculo)
calculo = 9 / 6
print(calculo)
# Exponencição nós usamos **
calculo = 3 ** 4
print(calculo)

#Lembrando que na matemáticas usamos () para indicar calculos
# que devem ser iniciados primeiro. Também que matemática
# funciona sempre da esquerda para direita.
# * > / >  + ou -

calculo2 = 2 + 4 - (3*2)
print(calculo2)
calculo2 = 2 / 2 + 4 - (3*2)
print(calculo2)
calculo2 =  4  * 20 / (1*5)
print(calculo2)